package org.dcm4chee.arc.entity;

import jakarta.persistence.*;

/**
 * @author Gunter Zeilinger (gunterze@protonmail.com)
 * @since Oct 2021
 */
@NamedQueries({
@NamedQuery(
        name=PatientDemographics.FIND_BY_PATIENT_ID,
        query="select pd from PatientDemographics pd " +
                "where pd.id like ?1")
})
@Entity
@Table(name = "patient_demographics")
public class PatientDemographics {

    public static final String FIND_BY_PATIENT_ID = "PatientDemographics.findByPatientID";

    @Id
    @Column(name = "pat_id")
    private String id;

    @Column(name = "pat_name")
    private String name;

    @Column(name = "pat_birthdate")
    private String birthDate;

    @Column(name = "pat_sex")
    private String sex;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    @Override
    public String toString() {
        return "PatientDemographics{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", birthDate='" + birthDate + '\'' +
                ", sex='" + sex + '\'' +
                '}';
    }
}
